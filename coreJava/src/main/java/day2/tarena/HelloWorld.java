package day2.tarena;
public class HelloWorld//每个词的意思？？？
{//类体{}，代表类的范围
	public static void main(String[] args)//java主方法，格式固定
	{//方法体，代表方法的范围
	    System.out.println("Hello World");
	}
}
//写好保存，用javac HelloWorld.java编译
//用java HelloWorld 运行
//方法三要素：方法名、参数列表、返回类型。
//方法名用来区分方法，main是主方法，jvm只会调用main。
//参数列表用来给方法传入数据，返回类型用来传出数据。无需
//传入用()，无需传出用void